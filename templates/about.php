    <h2>A <b>free</b> and <b>transparent</b> tool to support event organizers </h2>
    <p>
        At cBooking we work to deliver a free tool that can promote the organization of events, by
        supporting its logistics and registrations processes.
    </p>
    <p>
        Whoever is participating in an event can register himself and reach other available events.
        To make it as smooth as possible for you, dear organizer: we process your data, track registrations,
